public class Float {
    public static void main(String[] args) {
        // Positive, negative and zero
        float number =  3.1415f;
        System.out.println(number); // Prints 3.1415

        float number2 = -1.5f;
        System.out.println(number2); // Prints -1.5

        float number3 = 0.0f;
        System.out.println(number3); // Prints 0.0

        // Leading and closing zero is not mandatory
        float  number4 = 0.5f;
        System.out.println(number4); // Prints 0.5

        float number5 = 5.0f;
        System.out.println(number5); // Prints 5.0
    }
}

